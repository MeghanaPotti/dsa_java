package dsa_datatypes;

public class PrimitiveDataTypes {

	static int myNum = 5;               // Integer (whole number)
	static float myFloatNum = 5.99f;    // Floating point number
	static char myLetter = 'D';         // Character
	static boolean myBool = true;       // Boolean
	static String myText = "Hello";   
	
	public static void main(String[] args) {
		
		System.out.println("Number is "+myNum);
		System.out.println("Float Number is "+myFloatNum);
		System.out.println("Letter is "+myLetter);
		System.out.println("Boolean is "+myBool);
		System.out.println("Message is "+myText);
	
	}

}
